<h2>How is Business Calculus Different?</h2>
<p>Students who plan to go into science, engineering, or mathematics take a year-long sequence of classes that cover many of the same topics as we do in our one-quarter or one-semester course.</p>
<p>Here are some of the differences:</p>

<h3>No trigonometry</h3>
<p>We will not be using trigonometry at all in this course. The scientists and engineers need trigonometry frequently, and so a great deal of the engineering calculus course is devoted to trigonometric functions and the situations they can model.</p>

<h3>The applications are different</h3>
<p>
The scientists and engineers learn how to apply calculus to physics problems, such as work. They do a lot of geometric applications, like finding minimum distances, volumes of revolution, or arc-lengths. In this class, we will do only a few of these (distance/velocity problems, areas between curves). On the other hand, we will learn to apply calculus in some economic and business settings, like maximizing profit or minimizing average cost, finding elasticity of demand, or finding the present value of a continuous income stream. These are applications that are seldom seen in a course for engineers.</p>

<h3>Fewer theorems, no proofs</h3>
<p>The focus of this course is applications rather than theory. In this course, we will use the results of some theorems, but we won't prove any of them. When you finish this course, you should be able to solve many kinds of problems using calculus, but you won't be prepared to go on to higher mathematics.</p>

<h3>Less algebra</h3>
<p>In this class, you will not need clever algebra. If you need to solve an equation, it will either be relatively simple, or you can use technology to solve it. In most cases, you won't need <q>exact answers;</q> calculator numbers will be good enough.</p>
</section>

<section>
<h2>Simplification and Calculator Numbers</h2>

<p>When you were in tenth grade, your math teacher may have impressed you with the need to simplify your answers. I'm here to tell you &ndash; she was wrong. The form your answer should be in depends entirely on what you will do with it next. In addition, the process of <q>simplifying</q>, often messy algebra, can ruin perfectly correct answers. From the teacher’s point of view, <q>simplifying</q> obscures how a student arrived at his answer, and makes problems harder to grade. Moral: don't spend a lot of extra time simplifying your answer. Leave it as close to how you arrived at it as possible.</p>

<h3>When should you simplify?</h3>
<ul>
	<li>Simplify when it actually makes your life easier. For example, in Chapter 2 it's easier to find a second derivative if you simplify the first derivative.</li>
	<li>Simplify your answer when you need to match it to an answer in the book. You may need to do some algebra to be sure your answer and the book answer are the same.</li>
</ul>

<h3>When you use your calculator</h3>
<p>A calculator is required for this course, and it can be a wonderful tool. However, you should be careful not to rely too strongly on your calculator. Follow these rules of thumb:</p>
<ul>
	<li>Estimate your answers. If you expect an answer of about 4, and your calculator says 2500, you’ve made an error somewhere.</li>
	<li>Don’t round until the very end. Every time you make a calculation with a rounded number, your answer gets a little bit worse.</li>
	<li>When you answer an applied problem, find a calculator number. It doesn’t mean much to suggest that the company should produce \(\dfrac{2.4\cdot\sqrt{12100}}{2.5}\) items; it’s much more meaningful to report that they should produce about 106 items.</li>
	<li>When you present your final answer, round it to something that makes sense. If you’ve found an amount of US money, round it to the nearest cent. If you’ve computed the number of people, round to the nearest person. If there’s no obvious context, show your teacher at least two digits after the decimal place.</li>
	<li>Occasionally in this course, you will need to find the <q>exact answer</q>. That means <q>not a calculator approximation</q>. (You can still use your calculator to check your answer.)</li>
</ul>

