<h2>A Preview of Calculus</h2>
<p>Calculus was first developed more than three hundred years ago by Sir Isaac Newton and Gottfried Leibniz to help them describe and understand the rules governing the motion of planets and moons. Since then, thousands of other men and women have refined the basic ideas of calculus, developed new techniques to make the calculations easier, and found ways to apply calculus to problems besides planetary motion. Perhaps most importantly, they have used calculus to help understand a wide variety of physical, biological, economic and social phenomena and to describe and solve problems in those areas.</p>

<p>Part of the beauty of calculus is that it is based on a few very simple ideas. Part of the power of calculus is that these simple ideas can help us understand, describe, and solve problems in a variety of fields.</p>
</section>

